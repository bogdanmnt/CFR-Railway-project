package ro.enered.cfr.entities;

public class Locomotiva {
	
	private int id;
	private int nrMaxVag;
	private String marca;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNrMaxVag() {
		return nrMaxVag;
	}
	public void setNrMaxVag(int nrMaxVag) {
		this.nrMaxVag = nrMaxVag;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	
	
	
	
	
}
