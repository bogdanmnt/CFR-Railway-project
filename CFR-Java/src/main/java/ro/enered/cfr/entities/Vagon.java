package ro.enered.cfr.entities;

public class Vagon {
private int id;
private int nrLocuri;
private int clasa;
private double pret;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getNrLocuri() {
	return nrLocuri;
}
public void setNrLocuri(int nrLocuri) {
	this.nrLocuri = nrLocuri;
}
public int getClasa() {
	return clasa;
}
public void setClasa(int clasa) {
	this.clasa = clasa;
}
public double getPret() {
	return pret;
}
public void setPret(double pret) {
	this.pret = pret;
}

}
